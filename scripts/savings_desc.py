# Solution
# --------

savings = 100
growth_multiplier = 1.1
desc = "compound interest"

# Assign product of savings and 
# growth_multiplier to year1

year1 = savings * growth_multiplier

# Print the type of year1
print(type(year1))

# Assign sum of desc and desc to double_desc
double_desc = desc + desc

# Print out double_desc
print(double_desc)
